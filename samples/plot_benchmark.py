"""
Plot benchmark results from a CSV file generated by benchmark_models.py.
Uses only standard library + matplotlib/numpy (no pandas).
"""

import argparse
import csv
import logging
import sys
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np

logger = logging.getLogger(__name__)


def create_argparser() -> argparse.ArgumentParser:
    """Create argument parser for plotting script."""
    parser = argparse.ArgumentParser(
        description="Plot benchmark results from CSV file"
    )
    parser.add_argument(
        "-i", "--input", type=Path, default=Path("benchmark_results.csv"),
        help="Input CSV file (default: benchmark_results.csv)"
    )
    parser.add_argument(
        "-o", "--output", type=Path, default=Path("benchmark_plots.png"),
        help="Output plot file (default: benchmark_plots.png)"
    )
    parser.add_argument(
        "--show", action="store_true",
        help="Show plot interactively (default: save only)"
    )
    return parser


def format_metric(value: float, as_percent: bool = True) -> str:
    """Format metric value for bar labels.

    Args:
        value: Metric value (e.g., 0.1234)
        as_percent: If True, multiply by 100 and add '%' sign.

    Returns:
        Formatted string like '12.3%' or '1.2e-3%' for very small values.
    """
    if as_percent:
        val_pct = value * 100
        if val_pct >= 0.1:
            return f"{val_pct:.1f}%"
        return f"{val_pct:.1e}%"
    if value >= 0.001:
        return f"{value:.3f}"
    return f"{value:.1e}"


def load_data(
    csv_path: Path,
) -> tuple[list[str], list[float], list[float], list[float]]:
    """Load CSV and return labels, accuracy, macro_f1, avg_time.

    Raises:
        ValueError: If required columns are missing or data is invalid.
    """
    labels: list[str] = []
    accuracy: list[float] = []
    macro_f1: list[float] = []
    avg_time: list[float] = []

    with csv_path.open("r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        if reader.fieldnames is None:
            raise ValueError("CSV file has no header")
        required = {"donor", "accuracy", "macro_f1", "avg_time_per_image"}
        missing = required - set(reader.fieldnames)
        if missing:
            raise ValueError(f"Missing required columns: {missing}")

        has_clf_name = "classifier_name" in reader.fieldnames

        for row_num, row in enumerate(reader, start=2):
            donor = row.get("donor", "").strip()
            if not donor:
                raise ValueError(f"Row {row_num}: empty donor field")

            try:
                acc = float(row["accuracy"])
                f1 = float(row["macro_f1"])
                avg = float(row["avg_time_per_image"])
            except (ValueError, TypeError) as e:
                raise ValueError(
                    f"Row {row_num}: invalid numeric value - {e}"
                ) from e

            segment = row.get("segment", "").strip()
            clf_name = (
                row.get("classifier_name", "").strip()
                if has_clf_name else ""
            )

            if has_clf_name and clf_name:
                label = f"{donor} : {clf_name}"
            else:
                label = f"{donor} ({segment})" if segment else donor

            labels.append(label)
            accuracy.append(acc)
            macro_f1.append(f1)
            avg_time.append(avg)

    if not labels:
        raise ValueError("No valid data rows found in CSV")
    return labels, accuracy, macro_f1, avg_time


def plot_accuracy(ax: plt.Axes, labels: list[str], values: list[float]) -> None:
    """Plot accuracy bar chart."""
    x = np.arange(len(labels))
    bars = ax.bar(x, values, width=0.6, color="skyblue")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Accuracy")
    ax.set_title("Test Accuracy")
    ax.set_ylim(0, max(1.0, max(values) * 1.1))
    ax.bar_label(
        bars,
        fmt=lambda v: format_metric(v, as_percent=True),
        label_type="edge",
        padding=2,
    )


def plot_macro_f1(ax: plt.Axes, labels: list[str], values: list[float]) -> None:
    """Plot macro F1 bar chart."""
    x = np.arange(len(labels))
    bars = ax.bar(x, values, width=0.6, color="lightgreen")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Macro F1 Score")
    ax.set_title("Macro-averaged F1")
    ax.set_ylim(0, max(1.0, max(values) * 1.1))
    ax.bar_label(
        bars,
        fmt=lambda v: format_metric(v, as_percent=True),
        label_type="edge",
        padding=2,
    )


def plot_avg_time(ax: plt.Axes, labels: list[str], values: list[float]) -> None:
    """Plot average time per image bar chart (seconds)."""
    x = np.arange(len(labels))
    bars = ax.bar(x, values, width=0.6, color="salmon")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Seconds")
    ax.set_title("Average Time per Image")

    if max(values) / min(values) > 100:
        ax.set_yscale("log")
        for bar, val in zip(bars, values, strict=False):
            label = format_metric(val, as_percent=False) + "s"
            ax.text(
                bar.get_x() + bar.get_width() / 2,
                val,
                label,
                ha="center",
                va="bottom",
                fontsize=8,
            )
    else:
        ax.set_ylim(0, max(values) * 1.1)
        ax.bar_label(
            bars,
            fmt=lambda v: format_metric(v, as_percent=False) + "s",
            label_type="edge",
            padding=2,
        )


def plot_fps(ax: plt.Axes, labels: list[str], avg_time: list[float]) -> None:
    """Plot FPS bar chart (images per second)."""
    fps = [1.0 / t for t in avg_time]
    x = np.arange(len(labels))
    bars = ax.bar(x, fps, width=0.6, color="gold")
    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Images/sec")
    ax.set_title("Throughput (FPS)")

    if max(fps) / min(fps) > 100:
        ax.set_yscale("log")
        for bar, val in zip(bars, fps, strict=False):
            label = format_metric(val, as_percent=False)
            ax.text(
                bar.get_x() + bar.get_width() / 2,
                val,
                label,
                ha="center",
                va="bottom",
                fontsize=8,
            )
    else:
        ax.set_ylim(0, max(fps) * 1.1)
        ax.bar_label(
            bars,
            fmt=lambda v: format_metric(v, as_percent=False),
            label_type="edge",
            padding=2,
        )


def plot_metrics(
    labels: list[str],
    accuracy: list[float],
    macro_f1: list[float],
    avg_time: list[float],
    output_path: Path,
    show: bool = False,
) -> None:
    """Generate 2x2 subplot with all metrics."""
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle("Model Benchmark Comparison", fontsize=16)

    plot_accuracy(axes[0, 0], labels, accuracy)
    plot_macro_f1(axes[0, 1], labels, macro_f1)
    plot_avg_time(axes[1, 0], labels, avg_time)
    plot_fps(axes[1, 1], labels, avg_time)

    plt.tight_layout()
    plt.savefig(output_path, bbox_inches="tight", dpi=150)
    if show:
        plt.show()
    else:
        plt.close()
    logger.info(f"Plots saved to {output_path}")


def main() -> None:
    """Main entry point."""
    args = create_argparser().parse_args()
    try:
        if not args.input.exists():
            raise FileNotFoundError(f"Input file {args.input} not found.")
        data = load_data(args.input)
        plot_metrics(*data, args.output, args.show)
    except Exception:
        logger.exception("Fatal error in plotting script")
        sys.exit(1)


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    main()