#!/usr/bin/env python3
"""
Plot benchmark results from a CSV file generated by benchmark_models.py.
"""

import argparse
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd


def create_argparser() -> argparse.ArgumentParser:
    """Create argument parser for plotting script."""
    parser = argparse.ArgumentParser(
        description="Plot benchmark results from CSV file"
    )
    parser.add_argument(
        "-i", "--input", type=Path, default=Path("benchmark_results.csv"),
        help="Input CSV file (default: benchmark_results.csv)"
    )
    parser.add_argument(
        "-o", "--output", type=Path, default=Path("benchmark_plots.png"),
        help="Output plot file (default: benchmark_plots.png)"
    )
    parser.add_argument(
        "--show", action="store_true",
        help="Show plot interactively (default: save only)"
    )
    return parser


def load_data(csv_path: Path) -> pd.DataFrame:
    """Load CSV and drop rows with missing essential metrics."""
    df = pd.read_csv(csv_path)
    # Keep only rows that have non-empty donor and valid accuracy/macro_f1/avg_time
    df = df.dropna(subset=["donor", "accuracy", "macro_f1", "avg_time_per_image"])
    # Convert numeric columns
    df["accuracy"] = pd.to_numeric(df["accuracy"], errors="coerce")
    df["macro_f1"] = pd.to_numeric(df["macro_f1"], errors="coerce")
    df["avg_time_per_image"] = pd.to_numeric(df["avg_time_per_image"], errors="coerce")
    df = df.dropna(subset=["accuracy", "macro_f1", "avg_time_per_image"])
    # Create label: donor (segment)
    df["label"] = df["donor"] + " (" + df["segment"].fillna("full") + ")"
    return df


def plot_metrics(df: pd.DataFrame, output_path: Path, show: bool = False) -> None:
    """Generate 2x2 subplot with accuracy, macro F1, time per image, and FPS.

    Adds value labels on top of each bar.
    """
    fig, axes = plt.subplots(2, 2, figsize=(14, 10))
    fig.suptitle("Model Benchmark Comparison", fontsize=16)

    models = df["label"].tolist()
    x = np.arange(len(models))
    width = 0.6

    # Accuracy
    ax = axes[0, 0]
    bars_acc = ax.bar(x, df["accuracy"], width, color="skyblue")
    ax.set_xticks(x)
    ax.set_xticklabels(models, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Accuracy")
    ax.set_title("Test Accuracy")
    ax.set_ylim(0, max(1.0, df["accuracy"].max() * 1.1))
    # Add value labels: format as percentage with 1 decimal
    ax.bar_label(bars_acc, fmt="%.1f%%", label_type="edge", padding=2)

    # Macro F1
    ax = axes[0, 1]
    bars_f1 = ax.bar(x, df["macro_f1"], width, color="lightgreen")
    ax.set_xticks(x)
    ax.set_xticklabels(models, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Macro F1 Score")
    ax.set_title("Macro-averaged F1")
    ax.set_ylim(0, max(1.0, df["macro_f1"].max() * 1.1))
    ax.bar_label(bars_f1, fmt="%.1f%%", label_type="edge", padding=2)

    # Average time per image (seconds)
    ax = axes[1, 0]
    bars_time = ax.bar(x, df["avg_time_per_image"], width, color="salmon")
    ax.set_xticks(x)
    ax.set_xticklabels(models, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Seconds")
    ax.set_title("Average Time per Image")
    # Use log scale if values span orders of magnitude
    if df["avg_time_per_image"].max() / df["avg_time_per_image"].min() > 100:
        ax.set_yscale("log")
        # For log scale, bar labels need custom positioning (use same y value)
        for bar, val in zip(bars_time, df["avg_time_per_image"]):
            ax.text(bar.get_x() + bar.get_width() / 2, val, f"{val:.3f}s",
                    ha="center", va="bottom", fontsize=8)
    else:
        ax.set_ylim(0, df["avg_time_per_image"].max() * 1.1)
        ax.bar_label(bars_time, fmt="%.3fs", label_type="edge", padding=2)

    # FPS (images per second)
    # Recompute FPS from avg_time_per_image because
    # FPS is not stored in the original CSV (only avg_time_per_image is).
    fps = 1.0 / df["avg_time_per_image"]
    ax = axes[1, 1]
    bars_fps = ax.bar(x, fps, width, color="gold")
    ax.set_xticks(x)
    ax.set_xticklabels(models, rotation=45, ha="right", fontsize=8)
    ax.set_ylabel("Images/sec")
    ax.set_title("Throughput (FPS)")
    if fps.max() / fps.min() > 100:
        ax.set_yscale("log")
        for bar, val in zip(bars_fps, fps):
            ax.text(bar.get_x() + bar.get_width() / 2, val,
                    f"{val:.1f}" if val >= 10 else f"{val:.2f}",
                    ha="center", va="bottom", fontsize=8)
    else:
        ax.set_ylim(0, fps.max() * 1.1)

        # Use integer format for FPS > 1, else 2 decimals
        def fmt_fps(x):
            return f"{x:.0f}" if x >= 1 else f"{x:.2f}"

        ax.bar_label(bars_fps, fmt=fmt_fps, label_type="edge", padding=2)

    plt.tight_layout()
    plt.savefig(output_path, bbox_inches="tight", dpi=150)
    if show:
        plt.show()
    else:
        plt.close()
    print(f"Plots saved to {output_path}")


def main() -> None:
    """Main entry point."""
    args = create_argparser().parse_args()
    if not args.input.exists():
        print(f"Error: Input file {args.input} not found.")
        return

    df = load_data(args.input)
    if df.empty:
        print("No valid data rows found in CSV.")
        return

    plot_metrics(df, args.output, args.show)


if __name__ == "__main__":
    main()